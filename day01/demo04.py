
# In Python, data types are "inferred" at runtime.
#   This is called as "Dynamic Typing".
# type() function returns the type of given variable.

num = 10
print("num type=", type(num))

PI = 3.1415
print("PI type=", type(PI))

flag = True
print("flag type=", type(flag))

name = "Nilesh"
print("name type=", type(name))
